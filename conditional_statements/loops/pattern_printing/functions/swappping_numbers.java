public class swappping_numbers {
    public static void swap(int a,int b){
        int temp=a;
        a=b;
        b=temp;
        System.out.println("value of a is  "+ a +",value of b is   "+ b);
    }
    public static void main(String[] args) {
        swap(3, 6);
        
    }
    
}
